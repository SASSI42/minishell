int	meta_char_1(char c)
{
	if (c == ' ' || c == '\t' || c == '\n'
		|| c == '\r' || c == '\v' || c == '\f'
		|| c == '>' || c == '<' || c == '|'
		|| c == '"' || c == '\'')
		return (1);
	return (0);
}

